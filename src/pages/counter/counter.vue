<template>
  <div class="counter-warp">
    <p>Vuex counter：{{ count }}</p>
    <p>
      <button @click="increment">+</button>
      <button @click="decrement">-</button>
    </p>
    <button type="primary" @click="togo">去往下一页</button>
  </div>
</template>

<script>
import { mapState, mapMutations } from 'vuex'

export default {
  computed: {
    ...mapState([
      'count'
    ])
  },

  methods: {
    ...mapMutations([
      'increment',
      'decrement'
    ]),
    togo () {
      this.$router.push({ path: '/pages/list/index' })
    }
  }
}
</script>

